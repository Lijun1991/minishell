#include "libft.h"

int	word_count(char *str, char c)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if (str[i] != c)
			j++;
		while (str[i] != c)
		{
			i++;
			if (str[i] == '\0')
				return (j);
		}
		i++;
	}
	return (j);
}
