#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*t;
	size_t	i;

	i = 0;
	t = (char*)s;
	while (i < n)
	{
		if (t[i] == (char)c)
			return (&t[i]);
		i++;
	}
	return (NULL);
}
